package com.quanticheart.kotlingenerics

import android.os.Bundle
import androidx.appcompat.app.AppCompatActivity

class AnyOrStar : AppCompatActivity() {

    /**
     * https://www.baeldung.com/kotlin-generics
     */

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)
        val array = arrayOf(1, 2, 3, "go!")
        val array2 = arrayOf(1, 2, 3)

        printArray(array)

        printArray2(array)
        printArray2(array2)

        printArray3(array)
        printArray3(array2)
    }

    fun printArray(array: Array<*>) { // receive any types
        array.forEach { println(it) }
    }

    fun printArray2(array: Array<Any>) { // receive any types
        array.forEach { println(it) }
    }

    fun <T : Int> printArray3(array: Array<T>) { // receive only int
        array.forEach { println(it) }
    }
}
