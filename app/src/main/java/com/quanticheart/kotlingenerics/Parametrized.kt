package com.quanticheart.kotlingenerics

import android.os.Bundle
import androidx.appcompat.app.AppCompatActivity

class Parametrized : AppCompatActivity() {

    /**
     * https://www.baeldung.com/kotlin-generics
     */

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)

        val p = ParameterizedClass("test")
        val s = p.getValue()

        val f = parameterizedFun(1)
    }

    /**
     * Digamos que queremos criar uma classe parametrizada.
     * Podemos fazer isso facilmente na linguagem Kotlin usando tipos gen√©ricos:
     */
    class ParameterizedClass<A>(private val value: A) {
        fun getValue(): A {
            return value
        }
    }

    /**
     * Or
     */
    fun <A> parameterizedFun(value: A): A {
        return value
    }
}
